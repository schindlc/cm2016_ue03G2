package at.fhj.swd.confmana.calc.functions;

/**
 * NumToInt class to convert double to int
 */
public class NumToInt {
	/**
	 * converts double to int
	 * @param number double
	 * @return int
	 */
	public int numToInt(double number){
		// insert meaningful code here and adopt dummy return
		return new Double(number).intValue();
	}
}
